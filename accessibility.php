<h1>Accessibility</h1>

<p>Senior Citizens and people with disabilities readily use <a href="http://www.redwoodtransit.org">Redwood Transit System (RTS)</a>, <a href="http://www.eurekatransit.org">Eureka Transit Service (ETS)</a>, and <a href="http://www.arcatatransit.org/">Arcata & Mad River Transit Systems (AMRTS)</a>.</p>

<p>Use this page to learn how Transit is accessible to:</p>

<ul>
<li><a href="#mobility">people with limited mobility</a></li>
<li><a href="#low-vision">people who are blind or low-vision</a></li>
<li><a href="#deaf">people who are deaf or hard of hearing</a></li>
</ul>

<h4>Ride Wise travel tips</h4>

<p>If you are new to our bus system, there are several tips that will help making your trip a safe one.  <a href="http://www.redwoodtransit.org/how_to_ride.php">See "How to ride."</a></p>
		
<h4>Paratransit Services/Dial-a-Ride</h4>

<p>The certification process for travelers to with disabilities to use paratransit/dial-a-ride services in Humboldt County is performed through Humboldt Transit Authority. Complete and submit the appropriate form below to receive access to these services.</p>

<ul>
<li><p><b><a href="http://hta.org/dar/DAR Complete Application 2015.pdf">Eligibility requirements and application for RTS Dial-A-Ride Services [PDF]</a></b></p></li>
<li><p><b><a href="http://hta.org/dar/DAR_Unconditional_72_and_older-Application-2015.pdf">Application for Unconditional Dial-A-Ride / Dial-A-Lift Service Only [PDF]</a></b></p></li>
</ul>


<a name="mobility"></a><h3>Riders With Limited Mobility</h3>

<p>If you use a wheelchair or scooter, or have limited mobility, you can ride transit.</p>

<ul>
<li>All buses and transit centers are fully accessible.</li>
<li>You can board ETS, RTS, and AMRTS directly from the station platform.  Buses have a ramp that extends for easier boarding if you need it.</li>
<li>Buses are equipped with either a boarding ramp or power lift.  Some of our buses are low-floor buses that can “kneel”, lowering the first step closer to the curb for easier boarding.  Just ask the operator.</li>
<li>All buses have priority seating areas inside by the door for seniors and riders with limited mobility, and spaces for mobility devices.</li>
</ul>

<h4>Types of mobility devices allowed</h4>

<p><strong><i>Wheelchairs and Scooters</i></strong></p>

<p>The power lifts on buses can hold mobility devices up to 30 inches wide and 48 inches long, weighing less than 600 pounds when occupied.</p>

<strong><i>Scooters</i></strong>

<p>You can bring an electric scooters on the buses as long as they meet the same physical specifications of a common wheelchair. If your electric scooter can not be properly secured, the driver  will ask that you place your mobility device in the space provided on the bus.</p>

<h4>Securement straps for mobility devices on buses</h4>

<p>We offer special straps that attach to the corners of your mobility device, making securement on the bus easier and faster.  For more details call 707-443-0826 or email info@hta.org.</p>


<a name="low-vision"></a><h3>Riders Who are Blind or Low-Vision</h3>

<p>If you are blind or have a vision impairment, you can ride transit.</p>

<h4>Accessibility features</h4>

<ul>
<li>All ticket machines have audio/indicators for inserting bus tickets.</li>
<li>Bus drivers announce major bus stops and transfer points. ( You can also ask your operator to announce when your specific stop is coming.)</li>
<li>Service animals are permitted on all buses.  Learn more about bringing your service animal on board</li>
</ul>

<h4>Travel Aids Available</h4>

<ul>
<li><strong>Local: <a href="http://www.rosecoms.com/">Rose Communication Services</a></strong> provides training and services for the visually impaired.  Call to discuss your service needs at : 707-839-0588</li>
<li><strong>Location and amenity information for every stop and station</strong> is available for users of Sendero Group's BrailleNote or VoiceNote GPS wayfinding devices.</li>
</uL>

<h4>Data for BrailleNote and VoiceNote GPS Wayfinding Device</h4>

<a href="http://www.senderogroup.com/"><img src="images/braillenote.jpg" width="200" height="136" border="1" align="right" style="padding-left:6px;"/></a>


<p>Visually impaired riders using The Sendero Group BrailleNote or VoiceNote GPS wayfinding devices can access location and amenity information for every bus stop.</p>

<p>The <a href="http://www.senderogroup.com/">Sendero Group</a> includes transit information in its BrailleNote/VoiceNote User Points of Interest ("POI") database.</p>

<p>Braile Note/Voice Note User PQI download page
<br/>(<a href="http://www.mysendero.com/">www.mysendero.com</a>)</p>

<h5>How it works</h5>

<p>You can either "look around" by retrieving information relative to your GPS position or you can virtually explore an area before going there (this lets you review transit options before starting a trip).</p>

<p>In seconds, you'll know:</p>

<ul>
<li>the distance and direction to the nearest transit stop </li>
<li>which bus routes serve the stop </li>
</ul>


<h5>For more information</h5>

<p>If you are interested in acquiring a BrailleNote/VoiceNote GPS device, contact  Sendero Group at <a href="http://www.senderogroup.com/">www.senderogroup.com</a> or call 1-530-757-6800.</p>



<h4>Bringing a Service Animal on the Bus</h4>

<p>Service animals are welcome, but you may be asked to confirm that your animal is a service animal.</p>
<p>You are responsible for the care and supervision of your animal while on board. If you are planning on riding the bus with a service animal, please follow these guidelines:</p>

<ul>
<li>Your animal must be on a leash or in a pet carrier, remain under your control and behave appropriately. </li>
<li>The animal must remain at your feet. The animal may not sit on a vehicle seat. </li>
<li>The animal must not be aggressive toward people or other animals. </li>
<li>You are responsible for any damage or soiling caused by the animal.</li>
</ul>


<a name="deaf"></a><h3>Riders who are deaf or hard of hearing</h3>

<p>
<a href="http://ddtp.cpuc.ca.gov/default1.aspx?id=1482"><img src="http://hta.org/images/711.png" width="33" height="32" alt="7-1-1 Relay Service link" style="float:left;padding:6px;"/></a>If you have difficulty hearing on a standard telephone, dial 711 to reach the <a href="http://ddtp.cpuc.ca.gov/default1.aspx?id=1482">California Relay Service (CRS)</a> and reach Humboldt Transit Authority.  Humboldt Transit Authority's phone number is 707-443-0826.</a>
</p>


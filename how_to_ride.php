<h1>How to Ride</h1>


<p>Redwood Transit System (RTS) is an inter-city transit service providing commuter bus service along a 65 mile section of highway 101.   RTS provides transit service along US 101 corridor in Humboldt County and the cities of Rio Dell, Fortuna, Eureka, Arcata, and Trindad.</p>

<p>Also, the Humboldt Transit Authority (HTA) currently operates an intercity bus service link between Willow Creek and the coast.  This bus is designed to bring people into the area so that they can access the Arcata & Madriver Transit System (AMRTS), the Eureka Transit Service (ETS), and Redwood Transit System (RTS).</p>

<h3>How To Ride The Bus</h3>

<p>If you are new to our bus system, here are several things you should know before making your trip.</p>

<p><a href="how%20to%20ride%20pdfs/RTS_RIDER_GUIDE-Published.pdf">Redwood Transit Rider Guide PDF</a></p>
<p><a href="how%20to%20ride%20pdfs/SO_HUM_RIDERS%20GUIDE-Published.pdf">Southern Humboldt Rider Guide PDF</a></p>
<p><a href="how%20to%20ride%20pdfs/WILLOW_CREEK_RIDER_GUIDE-Published.pdf">Willow Creek Rider Guide PDF</a></p>

<p>Simple steps you can follow that help make your trip a safe one:</p>

<ul>
<li>Plan your route ahead of time.</li>

<li>Locate your origin and destination bus stops prior to your fist day of commuting on the bus.</li>

<li>Get to the bus stop at least five minutes early.  If you’re sitting on a bench, stand when you see the bus approaching to signal the driver to stop.</li>

<li>Never walk directly in front of or behind a bus.  The operator and other drivers may not see you.</li>

<li>Check the head sign listed above the windshield to make sure you board the right bus. </li>

<li>Have your correct fare ready. Drivers cannot take checks and do not carry change.</li>

<li>When the bus arrives, step aboard through the front door.  If you need any assistance with a connecting route, ask the driver.  Once completed, find a comfortable location for your ride.  </li>

<li>In slippery winter conditions, be extra careful getting on and off the bus. </li></li>

<li>As your stop approaches, ring the chime or pull the signal cord located along the interior windows.  This signals the driver to stop at the next stop.</li>

<li>Exit through the rear door.</li>
</ul>

<h3>Redwood Transit Bus Stops</h3>

<p>For your convenience, RTS  has installed bus stop signs along the north and south (101 corridor) RTS routes that indicate where you can board the bus. Also, the head sign will read the furthest north or south that the bus travels.  Since there may be other people standing by the stop who do not want to ride the bus, be sure to wave at the bus as it approaches. Flagging eliminates unnecessary stops and guarantees you faster, more efficient service.</p>


<h3>Bike Racks</h3>

<p>Our RTS buses are all equipped with bike racks in front of the bus, each holding two bikes. When the bus stops, lower the rack by gripping the center handle. Lift the bike handle (which has a hook on it for holding the bike), place the bike on the rack, and lower the bike handle over the bike to secure it. The handle should lift and lower to accommodate most bicycles. If you are having problems operating the bike racks, or the bike racks are full, or getting your bike on or off of them, ask your operator for assistance and they will help you.</p>

<h3>On the Bus</h3>

<ul> 
<li>Keep the aisle clear of tripping hazards such as bags, backpacks, and briefcases.</li>

<li>Stand back from the front of the bus so the operator has a clear view of the doorway and mirrors.</li>

<li>Sit where you can see your upcoming stop.</li>
</ul>

<p>For safety reasons, you cannot go barefoot on the bus, nor wear skates or rollerblades.  You you must wear a shirt.</p>

<h3>Children</h3>

<ul>
<li>Take extra care when traveling with young children.  Hold their hand when boarding and exiting.  Keep them seated to avoid falls and bumps, and watch that they don’t put their hands and head out the window. 
          
<li>Hold strollers securely, put on their brakes and keep the aisle clear, or hold your child and fold the stroller.
</ul>

<h3>Lost and Found</h3>

<p>Please check your seat before leaving the bus and make sure you have all your belongings.  However, if you do leave something, you may call the HTA office at 707-443-0826.  Every attempt will be made to recover your lost item. Lost and found items can be picked up at 133 "V" Street, Eureka.  Items will be kept up to 30 days, so please pick up your items within that time.</p>


<h3>Reduced Fares for Seniors & Disabled</h3>

<p>Reduced fares on fixed route buses are available to senior citizens and disabled persons who have been certified.  Forms are available at the HTA office.  I.D. cards from other systems will be honored.</p>


<h3>We can help</h3>

<ul>
<li>If you feel uncomfortable because of other individuals onboard, or are unsure about stops and bus riding logistics, sit near the front of the bus.</li>
<li>If you’re lost or feel sick, harrassed or threatened, speak to the operator who will help or radio for help.</li>
</ul>

<p>If you have any concerns please call 707-443-0826.</p>




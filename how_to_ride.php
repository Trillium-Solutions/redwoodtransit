<h1>How to Ride</h1>


<h4>Passenger Responsibility</h4>
<p>Traveling on Humboldt Transit Authorities buses usually involves interaction with the driver and other riders. It is every passenger's responsibility to behave in a manner that ensures the safety and comfort of all riders and drivers when traveling on a bus within Humboldt County.</p>

<h4><i>Passenger Rights</i></h4>
<ol>
	<li>To have a safe, comfortable and clean bus</li>
	<li>To have a courteous and knowledgeable bus operator </li>
	<li>To be provided with bus service that operates on schedule </li>
	<li>To have easy access to accurate schedule information </li>
	<li>To have stops called at transfer points, major intersections, points of interest and other requested locations</li>
	<li>To receive a prompt response to all reasonable comments and concerns on transit issues questions</li>
	<li>To be treated with dignity and respect by all HTA employees</li>
</ol>

<h4><i>Passenger Responsibilities</i></h4>
<ol>
	<li>Be prepared to board your bus </li>
	<li>Have exact change, pay the correct fare, and show proper identification when required </li>
	<li>Purchase a ticket from the driver or validate your card when boarding </li>
	<li>Keep your card or transfer ticket for your own use -Passengers must not lend cards or transfer tickets to another passenger </li>
	<li>Those passengers using mobility devices, use "Dial Down" feature when boarding the bus </li>
	<li>Keep aisles clear from obstruction </li>
	<li>Carry no more than three (3) shopping bags or more than you can handle in one single trip </li>
	<li>Avoid stowing bags and other items on seats </li>
	<li>Obey all posted rules, regulations, and ordinances </li>
	<li>Respect other passengers and the bus operator </li>
	<li>Help keep the bus clean and free of trash, spills, and graffiti </li>
	<li>Yield designated seats to elderly, disabled passengers, or mobility
devices</li>
	<li>Understand that service delays may result from circumstances that are beyond the control of the operator such as traffic delays or inclement weather </li>
	<li>Refrain from excessive noise and vulgar or offensive language </li>
	<li>Clean personal hygiene is a requirement to ride the bus </li>
	<li>Take extra care when traveling with young children</li>
		<ul>
			<li>Hold their hand when boarding and exiting</li>
		</ul>
</ol>

<h1>Humboldt Transit Authority Passenger Conduct</h1>

<p><center>The conduct of persons using the transit system is regulated by State Law, Federal Law, and the Humboldt Transit Authority Polices in order to make our system safe and secure for all customers. (Public Utilities Code Section 99170 and California Penal Code 640)</center></p>

<h4><i>Passenger Conduct</i></h4>
<ol>
	<li>No smoking or using e-cigs/vapor pens in buses, anywhere in a transit facility, or bus stops unless in a designated smoking area -Smoking within 25 feet of a bus shelter is prohibited </li>
	<li>No playing loud music or creating loud noise </li>
	<li>Harassing behavior will not be tolerated </li>
	<li>No littering or spitting </li>
	<li>Possessing any flammable liquid, unlawful firearms, hazardous materials (fireworks, batteries, or gasoline) is prohibited </li>
	<li>No consuming of an alcoholic beverage or being in possession of an open alcoholic beverage </li>
	<li>Persons involved in unauthorized or illegal activities are subject to prosecution and/or removal from all Humboldt Transit Authority Buses for a minimum of 30 days, up to permanent suspension</li>
	<li>Do NOT vandalize or create graffiti in or around buses or bus stops</li>
		<ul>
			<li>Vandalism is a crime</li>
		</ul>
</ol>

<h4><i>While on the bus</i></h4>
<ol>
	<li>Shirts and shoes must be worn on the bus </li>
	<li>Keep your belongings clear of the aisle and other seats </li>
	<li>Speak quietly when using mobile communication devices so as not to disturb other passengers </li>
	<li>No eating or drinking on the bus </li>
</ol>

<h4><i>Simple steps you can follow that help make your trip a safe one</i></h4>
<ol>
	<li>Plan your route ahead of time</li>
	<li>Locate your origin and destination bus stops prior to your fist day of commuting on the bus</li>
	<li>Get to the bus stop at least five minutes early.  If youâ€™re sitting on a bench, stand when you see the bus approaching and signal the driver to stop</li>
	<li>Never walk directly in front of or behind a bus.  The operator and other drivers may not see you</li>
	<li>Check the head sign listed above the windshield to make sure you board the correct bus</li>
	<li>Have your correct fare ready. Drivers cannot take checks, credit cards, and do not carry change</li>
	<li>If you need any assistance with a connecting route, ask the driver</li>
	<li>In slippery winter conditions, take extra care getting on and off the bus</li>
	<li>As your stop approaches, ring the chime or pull the signal cord located along the interior windows</li>
	<li>Exit through the rear door</li>
</ol>

<h4><i>Lost and Found</i></h4>
<p>HTA is not responsible for lost or stolen items. Please check your seat before leaving the bus and make sure you have all of your belongings. If you do leave something, you can call the HTA office.  Every attempt will be made to recover your lost item. Found items will be kept for 30 days.</p>

<h4><i>Reduced Fares for Seniors & Disabled</i></h4>
<p>Reduced fares on fixed route buses are available for senior citizens and disabled persons who have been certified.  Forms are available at the HTA office or on the HTA website.
<ul>
	<li>I.D. cards from other systems will be honored</li>
</ul>
</p>

<h4><i>Service Animals</i></h4>
<ol>
	<li>Service animals must be on a leash or harness </li>
		<ul>
			<li>Muzzles are not required</li>
		</ul>
<li>Must remain under passenger control at all times </li>
<li>Must remain at passengers feet or on passengers lap </li>
<li><b>MAY NOT</b> sit or lay on vehicle seat at anytime </li>
</ol>

<h4><i>Bicycles</i></h4>
<p>Bicycle racks are only provided on the RTS, Tish-Non Village, Willow Creek, So-Hum Local and Intercity transit systems. </p>
<ul>
	<li>Bicycles are to be stored on the exterior rack </li>
	<li>Bicycle racks can hold up to two bikes</li>
	<li>Passengers are responsible for loading and unloading bikes</li>
</ul>

<h4><i>Additional Information </i></h4>
<ul>
	<li>Strollers and walkers must be folded, placed under seat, and kept out of aisle</li>
	<li>Personal carts must fit between the seats, cannot have sharp edges, and passenger must be able to navigate cart without assistance</li>
</ul>

<h4><i>See Something, Say Something</i></h4>
<p>You can anonymously report any instance of vandalism, abuse, unsafe conditions, fraud or other problems you experience or observe by calling Humboldt Transit Authority.</p>





